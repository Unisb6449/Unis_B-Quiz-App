let currentQuestions = [];
let currentQuestionIndex = 0;
let score = 0;
let timer;
let timeLeft = 15;
let selectedAnswers = [];
let wrongAnswers = [];

function startQuiz() {
  document.getElementById("start-screen").style.display = "none";
  document.getElementById("department-screen").style.display = "block";
}

function selectDepartment(department) {
  const questions = allQuestions[department];
  currentQuestions = shuffleArray(questions).slice(0, 20);
  document.getElementById("department-screen").style.display = "none";
  document.getElementById("quiz-container").style.display = "block";
  showQuestion();
  startTimer();
}

function showQuestion() {
  clearInterval(timer);
  timeLeft = 15;
  startTimer();

  const q = currentQuestions[currentQuestionIndex];
  document.getElementById("question-box").innerHTML = `Q${currentQuestionIndex + 1}: ${q.question}`;

  const optionsBox = document.getElementById("options-box");
  optionsBox.innerHTML = "";
  q.options.forEach((opt, i) => {
    const btn = document.createElement("button");
    btn.className = "btn btn-outline-dark d-block mb-2 w-100 text-start";
    btn.innerText = opt;
    btn.onclick = () => selectAnswer(opt);
    optionsBox.appendChild(btn);
  });

  highlightSelected();
}

function selectAnswer(option) {
  selectedAnswers[currentQuestionIndex] = option;
  highlightSelected();
}

function highlightSelected() {
  const selected = selectedAnswers[currentQuestionIndex];
  const buttons = document.querySelectorAll("#options-box button");
  buttons.forEach(btn => {
    btn.classList.remove("active");
    if (btn.innerText === selected) {
      btn.classList.add("active");
    }
  });
}

function nextQuestion() {
  if (currentQuestionIndex < currentQuestions.length - 1) {
    currentQuestionIndex++;
    showQuestion();
  } else {
    endQuiz();
  }
}

function prevQuestion() {
  if (currentQuestionIndex > 0) {
    currentQuestionIndex--;
    showQuestion();
  }
}

function startTimer() {
  document.getElementById("timer").innerText = timeLeft;
  timer = setInterval(() => {
    timeLeft--;
    document.getElementById("timer").innerText = timeLeft;
    if (timeLeft === 0) {
      clearInterval(timer);
      nextQuestion();
    }
  }, 1000);
}

function endQuiz() {
  clearInterval(timer);
  score = 0;
  wrongAnswers = [];

  currentQuestions.forEach((q, i) => {
    const selected = selectedAnswers[i];
    if (selected === q.answer) {
      score++;
    } else {
      wrongAnswers.push({ ...q, selected });
    }
  });

  document.getElementById("quiz-container").style.display = "none";
  document.getElementById("result-screen").style.display = "block";

  document.getElementById("final-score").innerText = score;
  document.getElementById("correct-count").innerText = score;
  document.getElementById("wrong-count").innerText = 20 - score;
}

function showReview() {
  document.getElementById("result-screen").style.display = "none";
  document.getElementById("review-screen").style.display = "block";

  const reviewBox = document.getElementById("review-box");
  reviewBox.innerHTML = "";

  if (wrongAnswers.length === 0) {
    reviewBox.innerHTML = "<p class='text-success'>Awesome! You got all answers correct. 🎉</p>";
    return;
  }

  wrongAnswers.forEach((item, i) => {
    const div = document.createElement("div");
    div.className = "mb-3 p-3 border rounded";

    div.innerHTML = `
      <p><strong>Q${i + 1}:</strong> ${item.question}</p>
      <p><span class="text-danger">Your Answer:</span> ${item.selected || "No Answer"}</p>
      <p><span class="text-success">Correct Answer:</span> ${item.answer}</p>
    `;
    reviewBox.appendChild(div);
  });
}

function shuffleArray(arr) {
  return arr.sort(() => Math.random() - 0.5);
}
